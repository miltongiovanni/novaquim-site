<h5>4.6. PROCEDIMIENTOS Y MECANISMOS DE PROTECCIÓN DE LOS DERECHOS</h5>
<p>A continuación se describirán los términos y la forma de presentar: (i) consultas; (ii) reclamos o
    (iii) solicitudes de supresión de los Datos Personales. Los Titulares podrán iniciar estos
    procedimientos para garantizar sus derechos al habeas data o informar de un incumplimiento que, a su
    juicio, consideren grave en relación con las obligaciones a cargo de <strong>Industrias Novaquim
        S.A.S. </strong></p>
<h5>4.6.1. Consultas</h5>
<p>Los Titulares o los Autorizados podrán elevar consultas sobre cualquier información que repose en las
    Bases de Datos administradas por <strong>Industrias Novaquim S.A.S. </strong><strong> </strong> Una
    vez <strong>Industrias Novaquim S.A.S., </strong><strong> </strong>reciba la consulta adelantará los
    pasos que se describen a continuación. Tenga en cuenta que la consulta podrá enviarse por correo
    electrónico a la persona encargada en <strong>Industrias Novaquim
        S.A.S., </strong><strong> </strong>de atender los asuntos relacionados con la privacidad y la
    protección de Datos Personales. Preferiblemente el asunto de referencia deberá ser: “habeas data”.
</p>
<ol>
    <li><strong>Industrias Novaquim S.A.S. </strong>verificará la identidad del solicitante para
        determinar si está legitimado para elevar la consulta y si es, bajo parámetros razonables, el
        mismo suscriptor.
    </li>
    <li>Si el solicitante tiene las credenciales y/o autorizaciones para elevar la consulta, entonces
        <strong>Industrias Novaquim S.A.S. </strong>procederá a atenderla dentro de los siguientes diez
        (10) días hábiles, contados desde la recepción de la consulta.
    </li>
    <li>Si el solicitante no tiene las credenciales y/o autorizaciones correspondientes para consultar
        sobre ciertos Datos Personales, se le informará dentro de los diez (10) días hábiles, contados
        desde la recepción de la consulta. En este caso, se le dará la opción al solicitante que
        demuestre la capacidad o la autorización para elevar la consulta, aportando información
        adicional.
    </li>
    <li>Si en los puntos 3 y 4 la consulta no puede ser atendida dentro de los diez (10) días hábiles,
        se contactará al solicitante para comunicarle los motivos por los cuales el estado de la
        solicitud se encuentra en “trámite”. No obstante, la respuesta definitiva no podrá superar los
        cinco (5) días hábiles siguientes al vencimiento del primer término.
    </li>
    <li>Las respuestas serán enviadas preferentemente por correo electrónico o por un medio similar al
        cual fueron recibidas. En todo caso, las respuestas definitivas a todas las solicitudes no
        pueden tardar más de quince (15) días hábiles desde la fecha en la que la solicitud inicial fue
        recibida por <strong>Industrias Novaquim S.A.S. </strong></li>
    <li><strong>Industrias Novaquim S.A.S. </strong>guardará copia de todas las consultas, por si los
        Titulares o Autorizados desean copia de ellas eventualmente.
    </li>
</ol>
<h5>4.6.2. Reclamos</h5>
<p>Los Titulares o Autorizados podrán elevar reclamos en relación con los Datos Personales a <strong>Industrias
        Novaquim S.A.S., </strong><strong> </strong>y sus Encargados. Tenga en cuenta que los reclamos
    podrán enviarse por correo electrónico a la persona en <strong>Industrias Novaquim
        S.A.S., </strong><strong> </strong>encargada
    del tema de protección de Datos Personales. Tenga en cuenta que los reclamos podrán versar sobre:
</p>
<ul>
    <li>Información incorrecta.</li>
    <li>Ausencia de autorización o consentimiento en los términos de la Ley 1581 de 2012 o, en su
        defecto, del artículo 10 del Decreto 1377 de 2013.
    </li>
    <li>Información desactualizada o parcializada.</li>
    <li>Incumplimiento de <strong>Industrias Novaquim S.A.S., </strong>frente a los deberes u
        obligaciones de conformidad con el RGDPC.
    </li>
    <li>Falta de integridad o seguridad de la información (aduciendo las razones suficientes).</li>
    <li>Información que se desea suprimir.</li>
    <li>Revocatoria de la información.</li>
</ul>
<p>Para la interposición de reclamos se deberán tener en cuenta los siguientes pasos:</p>
<ol>
    <li><strong>Industrias Novaquim S.A.S.,</strong> verificará la identidad del solicitante para
        determinar si está legitimado para interponer el reclamo.
    </li>
    <li>El reclamo deberá contener los siguientes puntos: (i) nombre y apellido del Titular del Dato
        Personal; (ii) breve descripción de los hechos, y (iii) motivo o finalidad del reclamo.
        Adicionalmente, si es el caso, deberá anexar la documentación o pruebas pertinentes para
        sustentar el reclamo.
    </li>
    <li>Si el reclamo está incompleto, <strong>Industrias Novaquim S.A.S. </strong>podrá solicitarle al
        reclamante que subsane el reclamo dentro de los siguientes cinco (5) días hábiles, contados
        desde la fecha de recepción del reclamo. El reclamante tendrá un término de dos (2) meses para
        aportar y allegar la documentación exigida, de lo contrario se entenderá que el reclamante ha
        desistido del reclamo.
    </li>
    <li><strong>Industrias Novaquim S.A.S. </strong>dentro de los dos (2) días hábiles siguientes de la
        fecha de recepción del reclamo incluirá en su Base de Datos una leyenda que diga “reclamo en
        trámite” en los Datos Personales que están siendo objeto de reclamo.
    </li>
    <li><strong>Industrias Novaquim S.A.S. </strong>responderá el reclamo dentro de los siguientes
        quince (15) días hábiles contados a partir del día siguiente a la fecha de su recibo. Si no
        fuese posible atender el reclamo dentro de ese término, se le informará al interesado los
        motivos de la demora y la fecha en que se atenderá su reclamo. En todo caso, el reclamo no podrá
        superar los ocho (8) días hábiles siguientes al vencimiento del primer término.
    </li>
</ol>
<h5>4.6.3. Supresión</h5>
<p>El Titular tiene el derecho, en todo momento, a solicitarle a <strong>Industrias Novaquim
        S.A.S., </strong><strong> </strong>la supresión de sus Datos Personales siempre que:</p>
<ol>
    <li>Considere que los Datos Personales no están siendo tratados conforme a los principios, deberes y
        obligaciones previstas en el RGDPD.
    </li>
    <li>Hayan dejado de ser necesarios o pertinentes para las finalidades para las cuales fueron
        recolectados.
    </li>
    <li>Se haya superado el periodo necesario para el cumplimiento de los fines para los que fueron
        recolectados.
    </li>
</ol>
<p>Tenga en cuenta que si la solicitud de supresión es procedente, por no mediar un deber legal o
    contractual para mantener los Datos Personales, la consecuencia directa es la eliminación total o
    parcial de sus Datos Personales, sin los cuales en algunos casos no podrá obtener nuestros
    servicios.</p>
<p>El derecho de supresión no es absoluto y <strong>Industrias Novaquim
        S.A.S., </strong><strong> </strong>puede negar el ejercicio del mismo cuando:</p>
<ol>
    <li>Exista un deber legal o contractual de permanecer en la Base de Datos.</li>
    <li>La supresión pueda obstaculizar las actuaciones judiciales o administrativas vinculadas a
        obligaciones fiscales, la investigación y persecución de delitos o la actualización de sanciones
        administrativas.
    </li>
    <li>Los Datos Personales sean necesarios para proteger los intereses jurídicamente tutelados del
        Titular.
    </li>
    <li>Se vaya a realizar una acción en función del interés público.</li>
