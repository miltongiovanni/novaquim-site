<p><strong>Industrias Novaquim S.A.S., </strong>sociedad debidamente constituida conforme con las leyes
    colombianas, con domicilio en la ciudad de Bogotá D.C., dando cumplimiento al Decreto 1377 de 2013 y
    la normatividad vigente, informa la siguiente política aplicable a <strong>Industrias Novaquim
        S.A.S., </strong>para el tratamiento y protección de datos personales.</p>
<h4>1. OBJETIVO</h4>
<p>El propósito de esta Política es garantizar el derecho constitucional colombiano del habeas data a
    los Titulares y darles a conocer a estos cuáles son los lineamientos, directrices, procesos y
    medidas que ha implementado <strong>Industrias Novaquim S.A.S., </strong><strong> </strong>para
    demostrar su responsabilidad en relación con la protección de Datos Personales.</p>
<p>Esta Política busca fortalecer la cultura de la privacidad dentro de <strong>Industrias Novaquim
        S.A.S.</strong>, sus aliados comerciales y a quienes encarguen el Tratamiento de Datos
    Personales. En este sentido se señalan a continuación cuáles son las finalidades del Tratamiento de
    Datos Personales; los derechos de los Titulares, y los procedimientos dispuestos por <strong>Industrias
        Novaquim S.A.S. </strong>para hacer efectivos los derechos.</p>
<h4>2. ALCANCE</h4>
<p>Ésta Política le aplicará a cualquier Tratamiento realizado por <strong>Industrias Novaquim
        S.A.S. </strong>o por cualquier Encargado por cuenta de aquel que realice el Tratamiento de
    Datos Personales, dentro del territorio colombiano.</p>
<p>Ésta Política aplicará a todos los empleados, terceros, clientes o contratistas que realicen
    Tratamiento de Datos Personales en el desarrollo de su relación comercial o laboral con <strong>Industrias
        Novaquim S.A.S.</strong>, o a quienes se haya encargado una labor general o específica, a partir
    de la cual se derive un Tratamiento particular de Datos Personales. Esta Política no será aplicable
    cuando <strong>Industrias Novaquim S.A.S. </strong>actué como Encargado, pues el encargo supone la
    delimitación de las finalidades por parte del tercero que contrata y trasmite sus bases de datos a
    <strong>Industrias Novaquim S.A.S. </strong>dando instrucciones precisas frente al tratamiento que
    debe dársele a los Datos Personales. En este sentido cuando <strong>Industrias Novaquim
        S.A.S. </strong>sea Encargado del Tratamiento se someterá a la política de Tratamiento de la
    información del Responsable de los datos.</p>
<p>Finalmente, ésta Política le aplicará a los terceros con quienes <strong>Industrias Novaquim
        S.A.S. </strong>haya suscrito un contrato de transmisión o transferencia de Datos Personales o
    entregue información para el desarrollo de actividades particulares, siempre que <strong>Industrias
        Novaquim S.A.S. </strong>actúe en calidad de Responsable.</p>
<h4>3. DEFINICIONES</h4>
<p>Los términos relevantes de esta política de Tratamiento de la información tendrán el significado que
    a continuación se le asigna, o aquel que precede al término entrecomillas dentro del paréntesis.</p>
<p><strong>Autorizado:</strong> Son aquellas personas que pueden ejercer los derechos del Titular, tales
    como, el Titular, acreditando su identidad por los medios que tenga a su disposición, los
    causahabientes que acrediten esa calidad, el representante y/o apoderado acreditándose mediante
    apoderamiento o representación legal y aquellos que, por estipulación a favor de otro o para otro
    estén acreditados.</p>
<p><strong>Autorización:</strong> Es la comunicación verbal o escrita generada por <strong>Industrias
        Novaquim S.A.S. </strong>dirigida a los Titulares para informarle de la existencia de esta
    Política, la forma de acceder a ella y por medio de la cual haciendo click realiza una conducta
    inequívoca autorizando el Tratamiento de sus Datos Personales para las finalidades y en los términos
    de esta Política.</p>
<p><strong>Base de Datos:</strong> Conjunto organizado de Datos Personales que sean recolectados,
    encargados y tratados por <strong>Industrias Novaquim S.A.S.</strong></p>
<p><strong>Dato Privado</strong>: aquel que por su naturaleza íntima o reservada sólo es relevante para
    el Titular.</p>
<p><strong>Dato Público:</strong> Es aquel que no sea semiprivado, privado o sensible. Son considerados
    datos públicos aquellos relativos al estado civil de las personas, su profesión, el número de
    cédula, y otros datos contenidos en registros públicos, documentos públicos, gacetas y boletines
    oficiales.</p>
<p><strong>Dato Semiprivado:</strong> Es aquel que no tiene naturaleza íntima, reservada, ni pública y
    cuyo conocimiento o divulgación puede interesar no sólo a su Titular sino a cierto sector o grupo de
    personas o a la sociedad en general, como el dato financiero y crediticio de actividad comercial.
</p>
<p><strong>Datos Personales:</strong> Es cualquier información vinculada o que pueda asociarse a una o
    varias personas naturales determinadas o determinables. Adicionalmente, el Dato Personal debe
    permitir, en mayor o menor medida, la identificación del Titular. Estos incluyen pero no se limitan
    a nombre, apellido, correo electrónico, teléfono, dirección y cualquier información vinculada al
    Titular.</p>
<p><strong>Datos Sensibles</strong>: Es aquel que afecta la intimidad del Titular y cuyo Tratamiento
    puede generar discriminación, tales como, aquellos que revelen el origen racial o étnico,
    pertenencia a sindicatos, ideología política, religiosa o filosófica, datos sobre la salud y datos
    biométricos.</p>
<p><strong>Encargado:</strong> Es la persona natural o jurídica, pública o privada, que por sí o en
    asocio con otros realiza el Tratamiento de Datos Personales por encargo de un Responsable. En
    relación con los contratos estatales que suscribe <strong>Industrias Novaquim S.A.S. </strong>con
    las entidades públicas, éste funge de encargado en la medida que recibe instrucciones y lineamientos
    con base en el objeto contractual.</p>
<p><strong>Entidad Pública:</strong> Son la Nación, las regiones, los departamentos, las provincias, el
    Distrito Capital y los distritos especiales, las áreas metropolitanas, las asociaciones de
    municipios, los territorios indígenas y los municipios; los establecimientos públicos, las empresas
    industriales y comerciales del Estado, las sociedades de economía mixta en las que el Estado tenga
    participación superior al cincuenta por ciento (50%), así como las entidades descentralizadas
    indirectas y las demás personas jurídicas en las que exista dicha participación pública mayoritaria,
    cualquiera sea la denominación que ellas adopten, en todos los órdenes y niveles con quienes
    <strong>Industrias Novaquim S.A.S. </strong>tenga alguna concesión o haya suscrito un contrato
    estatal para el apoyo de las actividades de las entidades.</p>
<p><strong>Política de Tratamiento de la Información:</strong> Es el presente documento que contiene los
    lineamientos del Tratamiento de Datos Personales que surjan en cualquier tipo de Tratamiento que
    realice <strong>Industrias Novaquim S.A.S. </strong>sobre los Datos Personales. Particularmente,
    este incluye las finalidades, los tipos de Tratamiento, los derechos de los Titulares, los
    procedimientos para garantizar esos derechos y las personas dentro de <strong>Industrias Novaquim
        S.A.S. </strong>encargadas de atender las consultas y/o reclamos.</p>
<p><strong>Responsable:</strong> Es la persona natural o jurídica, pública o privada, que por sí misma o
    en asocio con otros, decida sobre la Base de Datos, el Tratamiento de los Datos Personales y/o los
    fines y condiciones del Tratamiento. Para estos efectos, el Responsable será <strong>Industrias
        Novaquim S.A.S.</strong>, sin embargo teniendo en cuenta las actividades comerciales de <strong>Industrias
        Novaquim S.A.S. </strong><strong> </strong>éste actuará como Encargado cuando realice algún
    Tratamiento sobre las Bases de Datos que le remitan las entidades que contratan sus servicios,
    siempre que éstas determinen las finalidades y lineamientos del tratamiento.</p>
<p><strong>RGDPC o Régimen General de Protección de Datos Personales colombiano</strong>: Es el conjunto
    de normas que regula el Tratamiento de Datos Personales en el territorio colombiano tales como, el
    artículo 15 de la Constitución Política colombiana; la Ley 1581 de 2012; el Decreto 1377 de 2013; el
    Decreto 886 de 2014; la Sentencia C-748 de 2011, y las demás normas que las modifiquen o adicionen.
</p>
<p><strong>Titular:</strong> Es el empleado, cliente, proveedor, contratista, trabajador o cualquier
    persona natural determinada o determinable cuya información y Datos Personales sean tratados.</p>
<p><strong>Transferencia:</strong> Es el Tratamiento de Datos Personales que tiene lugar cuando el
    Responsable y/o Encargado del Tratamiento de Datos Personales, ubicado en Colombia, envía la
    información o los Datos Personales a un receptor, que a su vez es Responsable del Tratamiento y se
    encuentra dentro o fuera del país.</p>
<p><strong>Transmisión:</strong> Es el Tratamiento de Datos Personales que implica la comunicación de
    los mismos dentro o fuera del territorio de la República de Colombia cuando tenga por objeto la
    realización de un Tratamiento por el Encargado por cuenta del Responsable.</p>
<p><strong>Tratamiento:</strong> Es cualquier operación o conjunto de operaciones sobre Datos
    Personales, tales como la recolección, administración, almacenamiento, uso, circulación,
    transmisión, transferencia o supresión.</p>
<h4>4. POLÍTICAS PARA TRATAMIENTO DE DATOS PERSONALES</h4>
<p>Industrias Novaquim S.A.S. se compromete a llevar a cabo todo Tratamiento de Datos Personales de
    acuerdo con los siguientes principios que no sólo aplicarán a la información de carácter personal,
    sino también a la información que no quede cobijada directamente por el RGDPC.</p>
<p><strong>Circulación Restringida</strong>: El Tratamiento de Datos Personales sólo se llevará a cabo
    por el personal autorizado por Industrias Novaquim S.A.S. o por terceros que deberán someterse a los
    lineamientos, obligaciones y finalidades consignadas en la presente Política.</p>
<p><strong>Confidencialidad</strong>: El Tratamiento de la información se sujetará a la confidencialidad
    que deberán guardar todos aquellos que tengan acceso o puedan manipular los Datos Personales. Este
    principio no sólo estará vigente durante la relación con el Titular, sino incluso después de ella.
    Únicamente se podrá comunicar o suministrar la información cuando el ejercicio laboral o una
    obligación así lo exija.</p>
<p><strong>Finalidad</strong>: El Tratamiento obedecerá a una finalidad legítima de acuerdo con la
    Constitución Política de Colombia, las leyes aplicables y demás normas que las desarrollen, la cual
    será debidamente informada a los Titulares.</p>
<p><strong>Libertad</strong>: El Tratamiento se ejercerá únicamente con el consentimiento previo,
    expreso e informado del Titular. Los Datos Personales que sean recolectados sin el mencionado
    consentimiento estarán autorizados por la Ley o por la implementación de mecanismos eficientes de
    comunicación, sin perjuicio de la solicitud de supresión de los Datos Personales que eventualmente
    pueda ser elevada por el Titular y que proceda por no mediar una obligación legal o contractual que
    lo impida.</p>
<p><strong>Seguridad</strong>: El Tratamiento de la información se sujetará a medidas físicas y
    tecnológicas de seguridad para evitar el acceso no autorizado a los Datos Personales o la
    adulteración o pérdida de los mismos.</p>
<p><strong>Transparencia</strong>: Industrias Novaquim S.A.S., adoptará los estándares de transparencia
    necesarios para que el Titular siempre pueda acceder a sus Datos Personales y tenga conocimiento
    pleno e información completa sobre sus Datos Personales y los usos que se le estén dando a los
    mismos.</p>
<p><strong>Veracidad</strong>: La información sometida a Tratamiento buscará ser un fiel reflejo de la
    realidad, razón por la cual se implementarán medidas para mantener la información actualizada y
    completa, y evitar datos o fragmentos que induzcan al error.</p><h4>4.1. DERECHO AL HABEAS DATA DE
    LOS TITULARES</h4>
<p>Esta Política informa y garantizará el derecho al habeas data de los Titulares como la facultad al
    Titular de Datos Personales de exigir a los Responsables de los Datos Personales el acceso,
    inclusión, exclusión, corrección, adición, actualización y certificación de los datos. En este
    sentido, y de acuerdo con el RGDPC, el Titular tiene los siguientes derechos:</p>
<ol>
    <li>Conocer, actualizar, modificar y rectificar los Datos Personales frente a los Responsables o
        Encargados del Tratamiento de manera gratuita, de acuerdo con lo señalado en el artículo 21 del
        Decreto 1377 de 2013.
    </li>
    <li>Solicitar prueba de la autorización otorgada a <strong>Industrias Novaquim S.A.S. </strong>, o,
        en su defecto, la implementación de los mecanismos eficientes o alternos de comunicación, de
        conformidad con el artículo 10 del Decreto 1377 de 2013. Esta autorización puede ser otorgada de
        manera verbal, escrita o por medio de conductas concluyentes como a través de un clic aceptando
        las condiciones y términos de uso de la página web y de esta Política en caso de los usuarios,
        por lo cual dicha copia puede ser un pantallazo u otro medio que pruebe el otorgamiento de la
        autorización.
    </li>
    <li>Solicitar información sobre los usos y las finalidades a las que están siendo sometidos sus
        Datos Personales.
    </li>
    <li>Presentar quejas por infracciones ante la Superintendencia de Industria y Comercio en materia de
        Datos Personales, previa solicitud, consulta o reclamo dirigido a <strong>Industrias Novaquim
            S.A.S. </strong>o al Encargado del Tratamiento de los Datos Personales.
    </li>
    <li>Revocar la autorización y/o solicitar la supresión de sus Datos Personales y su almacenamiento
        en bases de datos de <strong>Industrias Novaquim S.A.S. </strong>, siempre y cuando no exista un
        deber legal o contractual que impida el ejercicio de este derecho de supresión.
    </li>
    <li>Acceder en forma gratuita a los Datos Personales que hayan sido recolectados por <strong>Industrias
            Novaquim S.A.S.</strong></li>
    <li>Los demás derechos que sean conferidos por el Régimen de Protección de Datos Personales.</li>
</ol>
<p>Los anteriores derechos únicamente podrán ejercerse por aquellos Titulares o Autorizados que
    acrediten de manera suficiente con la documentación pertinente o credenciales confiables su
    identificación.</p><h4>4.2. DATOS SENSIBLES</h4>
